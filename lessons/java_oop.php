<h3>Object-Oriented Programming (OOP) in Java</h3>
<p>OOP is a programming paradigm based on objects and classes. In Java, everything is an object. Key OOP principles are:</p>
<ul>
  <li><b>Encapsulation</b>: Bundling data and methods together.</li>
  <li><b>Abstraction</b>: Hiding complex details and showing only essentials.</li>
  <li><b>Inheritance</b>: Acquiring properties from another class.</li>
  <li><b>Polymorphism</b>: One interface, many implementations.</li>
</ul>
